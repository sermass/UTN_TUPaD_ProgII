/*
 Programa para observar la utilizacion de variables varia
 */
package variables_java;


public class Variables_Java {

    /**
     * @param args the command line arguments
     */
    public static void main(String[] args) {
        
        // Definicion de variables y asignacion de valores
        String nombre;
        nombre = "Sergio";
        
        int edad = 35;        
        double altura = 1.75;        
        boolean estudiante = true;        
        
        //Salida por consola
        System.out.println("Nombre: " + nombre + ", Edad: " + edad + ", Altura: " + altura + ", Estudiante: " + estudiante);
    }
    
}
